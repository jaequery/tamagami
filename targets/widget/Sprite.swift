// MARK: - Sprite lookup

func getSprite(stage: LifeStage, mood: Mood) -> SpriteMatrix {
    if mood == .dead { return SPRITE_DEAD }
    if stage == .egg  { return SPRITE_EGG }

    switch stage {
    case .egg:
        return SPRITE_EGG
    case .baby:
        switch mood {
        case .happy:    return SPRITE_BABY_HAPPY
        case .sad:      return SPRITE_BABY_SAD
        case .sick:     return SPRITE_BABY_SICK
        case .sleeping: return SPRITE_BABY_SLEEPING
        default:        return SPRITE_BABY_NEUTRAL
        }
    case .child:
        switch mood {
        case .happy:    return SPRITE_CHILD_HAPPY
        case .sad:      return SPRITE_CHILD_SAD
        case .sick:     return SPRITE_CHILD_SICK
        case .sleeping: return SPRITE_CHILD_SLEEPING
        default:        return SPRITE_CHILD_NEUTRAL
        }
    case .teen:
        switch mood {
        case .happy:    return SPRITE_TEEN_HAPPY
        case .sad:      return SPRITE_TEEN_SAD
        case .sick:     return SPRITE_TEEN_SICK
        case .sleeping: return SPRITE_TEEN_SLEEPING
        default:        return SPRITE_TEEN_NEUTRAL
        }
    case .adult:
        switch mood {
        case .happy:    return SPRITE_ADULT_HAPPY
        case .sad:      return SPRITE_ADULT_SAD
        case .sick:     return SPRITE_ADULT_SICK
        case .sleeping: return SPRITE_ADULT_SLEEPING
        default:        return SPRITE_ADULT_NEUTRAL
        }
    }
}

// MARK: - SwiftUI renderer

/// Renders a 14×14 sprite matrix as a grid of SwiftUI Rectangles.
/// `cellSize` controls the pt dimension of each pixel cell.
struct PixelSprite: View {
    let stage: LifeStage
    let mood: Mood
    var cellSize: CGFloat = 3.0

    private var matrix: SpriteMatrix { getSprite(stage: stage, mood: mood) }

    var body: some View {
        VStack(spacing: 0) {
            ForEach(0..<matrix.count, id: \.self) { row in
                HStack(spacing: 0) {
                    ForEach(0..<matrix[row].count, id: \.self) { col in
                        Rectangle()
                            .fill(paletteColor(matrix[row][col]))
                            .frame(width: cellSize, height: cellSize)
                    }
                }
            }
        }
    }
}

/// A simplified single-color (monochrome) sprite for Lock Screen accessory families.
/// Renders dark pixels as the accent tint; transparent pixels are clear.
struct MonochromeSprite: View {
    let stage: LifeStage
    let mood: Mood
    var cellSize: CGFloat = 2.5

    private var matrix: SpriteMatrix { getSprite(stage: stage, mood: mood) }

    var body: some View {
        VStack(spacing: 0) {
            ForEach(0..<matrix.count, id: \.self) { row in
                HStack(spacing: 0) {
                    ForEach(0..<matrix[row].count, id: \.self) { col in
                        let idx = matrix[row][col]
                        Rectangle()
                            .fill(idx == 0 ? Color.clear : Color.primary)
                            .frame(width: cellSize, height: cellSize)
                    }
                }
            }
        }
        .widgetAccentable()
    }
}

// MARK: - Color extension

extension Color {
    init(hex: String) {
        let h = hex.hasPrefix("#") ? String(hex.dropFirst()) : hex
        let scanner = Scanner(string: h)
        var value: UInt64 = 0
        scanner.scanHexInt64(&value)
        let r = Double((value >> 16) & 0xFF) / 255
        let g = Double((value >>  8) & 0xFF) / 255
        let b = Double( value        & 0xFF) / 255
        self.init(red: r, green: g, blue: b)
    }
}
